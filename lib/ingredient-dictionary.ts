// ── Helpers ───────────────────────────────────────────────────────────────

export function isNorwegianDomain(url: string): boolean {
  try {
    const hostname = new URL(url).hostname.toLowerCase()
    return NORWEGIAN_DOMAINS.some(d => hostname === d || hostname.endsWith(`.${d}`))
  } catch {
    return false
  }
}

export function translateToNorwegian(name: string): string | null {
  return INGREDIENT_DICTIONARY[name.toLowerCase().trim()] ?? null
}
